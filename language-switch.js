// Текущий язык (по умолчанию русский)
let currentLang = 'ru';

// Функция для обновления текста на странице
function updateLanguage(lang) {
    // Обновляем атрибут lang в теге html
    document.documentElement.lang = lang;

    // Обновляем заголовок страницы
    document.title = translations[lang].title;

    // Обновляем бегущую строку
    document.querySelector('.marquee span').textContent = translations[lang].marquee;

    // Обновляем hero-секцию
    const hero = document.querySelector('.hero');
    hero.querySelectorAll('h1')[0].textContent = translations[lang].hero.h1_1;
    hero.querySelectorAll('h1')[1].textContent = translations[lang].hero.h1_2;
    const heroParagraphs = hero.querySelectorAll('p');
    heroParagraphs[0].textContent = translations[lang].hero.p1;
    heroParagraphs[1].textContent = translations[lang].hero.p2;
    heroParagraphs[2].textContent = translations[lang].hero.p3;
    heroParagraphs[3].textContent = translations[lang].hero.p4;
    heroParagraphs[4].textContent = translations[lang].hero.p5;
    hero.querySelector('textPath').textContent = translations[lang].hero.svg_text;
    hero.querySelector('text[x="100"]').textContent = translations[lang].hero.svg_center;

    // Обновляем секцию "Кто мы"
    const about = document.querySelector('.about');
    about.querySelector('h2').textContent = translations[lang].about.h2;
    const aboutParagraphs = about.querySelectorAll('p');
    aboutParagraphs[0].textContent = translations[lang].about.p1;
    aboutParagraphs[1].textContent = translations[lang].about.p2;
    aboutParagraphs[2].textContent = translations[lang].about.p3;
    aboutParagraphs[3].textContent = translations[lang].about.p4;
    aboutParagraphs[4].textContent = translations[lang].about.p5;

    // Обновляем секцию "Что ты получишь"
    const benefits = document.querySelector('.benefits-grid').parentElement;
    benefits.querySelector('h2').textContent = translations[lang].benefits.h2;
    const tiles = benefits.querySelectorAll('.tile p');
    tiles[0].textContent = translations[lang].benefits.tile1;
    tiles[1].textContent = translations[lang].benefits.tile2;
    tiles[2].textContent = translations[lang].benefits.tile3;
    tiles[3].textContent = translations[lang].benefits.tile4;
    tiles[4].textContent = translations[lang].benefits.tile5;
    tiles[5].textContent = translations[lang].benefits.tile6;
    tiles[6].textContent = translations[lang].benefits.tile7;
    tiles[7].textContent = translations[lang].benefits.tile8;

    // Обновляем секцию "Ты нам подойдёшь, если"
    const fit = document.querySelectorAll('.glass')[2];
    fit.querySelector('h2').textContent = translations[lang].fit.h2;
    const fitItems = fit.querySelectorAll('li');
    fitItems[0].textContent = translations[lang].fit.li1;
    fitItems[1].textContent = translations[lang].fit.li2;
    fitItems[2].textContent = translations[lang].fit.li3;
    fitItems[3].textContent = translations[lang].fit.li4;
    fitItems[4].textContent = translations[lang].fit.li5;

    // Обновляем секцию "Требования"
    const requirements = document.querySelectorAll('.glass')[3];
    requirements.querySelector('h2').textContent = translations[lang].requirements.h2;
    const reqItems = requirements.querySelectorAll('li');
    reqItems[0].textContent = translations[lang].requirements.li1;
    reqItems[1].textContent = translations[lang].requirements.li2;
    reqItems[2].textContent = translations[lang].requirements.li3;
    reqItems[3].textContent = translations[lang].requirements.li4;

    // Обновляем форму на странице
    const formSection = document.querySelectorAll('.glass')[4];
    formSection.querySelector('h2').textContent = translations[lang].form.h2;
    const formInputs = formSection.querySelectorAll('input');
    formInputs[0].placeholder = translations[lang].form.placeholder1;
    formInputs[1].placeholder = translations[lang].form.placeholder2;
    formInputs[2].placeholder = translations[lang].form.placeholder3;
    formInputs[3].placeholder = translations[lang].form.placeholder4;
    formSection.querySelector('button').textContent = translations[lang].form.button;
    formSection.querySelector('p').textContent = translations[lang].form.note;
    formSection.querySelector('a').textContent = translations[lang].form.privacy;

    // Обновляем модальное окно
    const modal = document.querySelector('.modal');
    modal.querySelector('h2').textContent = translations[lang].modal.h2;
    const modalInputs = modal.querySelectorAll('input');
    modalInputs[0].placeholder = translations[lang].modal.placeholder1;
    modalInputs[1].placeholder = translations[lang].modal.placeholder2;
    modalInputs[2].placeholder = translations[lang].modal.placeholder3;
    modalInputs[3].placeholder = translations[lang].modal.placeholder4;
    modal.querySelector('button').textContent = translations[lang].modal.button;
    modal.querySelector('p').textContent = translations[lang].modal.note;
    modal.querySelector('a').textContent = translations[lang].modal.privacy;

    // Обновляем секцию контактов
    const contacts = document.querySelector('.contacts');
    contacts.querySelector('h2').textContent = translations[lang].contacts.h2;
    contacts.querySelector('.telegram-btn').textContent = translations[lang].contacts.telegram;
    contacts.querySelector('.lang-switch-btn').textContent = translations[lang].contacts.lang_switch;

    // Обновляем фиксированную кнопку
    document.querySelector('.fixed-btn').textContent = translations[lang].fixed_btn;

    // Сохраняем текущий язык
    currentLang = lang;
}

// Обработчик клика по кнопке смены языка
document.querySelector('.lang-switch-btn').addEventListener('click', (e) => {
    e.preventDefault();
    const newLang = currentLang === 'ru' ? 'ua' : 'ru';
    updateLanguage(newLang);
});

